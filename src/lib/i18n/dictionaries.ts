export function translate(
  locale: SiteLocale,
  key: string,
  vars?: Record<string, string | number>,
): string {
  const table = DICTIONARY[locale] ?? DICTIONARY.ko;
  let s = table[key] ?? DICTIONARY.ko[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.split(`{${k}}`).join(String(v));
    }
  }
  return s;
}

/** `/category/{slug}` → `nav.cat.{slug}` */
export function categoryNavKey(href: string): string {
  const slug = href.replace(/^\/category\//, "").split("/")[0] ?? "";
  return `nav.cat.${slug || "best"}`;
}
